        System.out.println("\nLIFESTYLE MODIFICATIONS:");
        System.out.println("1. Exercise regularly (both aerobic and resistance training)");
        System.out.println("2. Maintain a healthy weight");
        System.out.println("3. Monitor blood glucose levels as recommended by your healthcare provider");
        System.out.println("4. Take medications as prescribed");
        System.out.println("5. Manage stress through relaxation techniques");
        System.out.println("6. Get adequate sleep (7-8 hours per night)");
        System.out.println("7. Attend regular check-ups with your healthcare team");
        System.out.println("8. Practice good foot care and skin care");
        System.out.println("9. Quit smoking if you smoke");

        System.out.println("\nDIABETES-FRIENDLY FOODS TO EMPHASIZE:");
        System.out.println("- Non-starchy vegetables (leafy greens, broccoli, peppers, etc.)");
        System.out.println("- Whole grains (brown rice, quinoa, whole wheat bread)");
        System.out.println("- Legumes (beans, lentils, chickpeas)");
