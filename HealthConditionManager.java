        System.out.println("4. Limit alcohol consumption (max 1 drink per day for women, 2 for men)");
        System.out.println("5. Consider reducing caffeine intake");

        System.out.println("\nLIFESTYLE MODIFICATIONS:");
        System.out.println("1. Maintain a healthy weight or lose weight if overweight");
        System.out.println("2. Exercise regularly (aim for 150 minutes of moderate activity per week)");
